import assert from 'node:assert/strict';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';
import test from 'node:test';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = fileURLToPath(new URL('../scripts/', import.meta.url));

function createWorkspace(t, files) {
  const workspace = fs.mkdtempSync(path.join(os.tmpdir(), 'context-engineering-template-'));
  t.after(() => fs.rmSync(workspace, { recursive: true, force: true }));

  for (const [file, content] of Object.entries(files)) {
    const full = path.join(workspace, ...file.split('/'));
    fs.mkdirSync(path.dirname(full), { recursive: true });
    fs.writeFileSync(full, content, 'utf8');
  }

  return workspace;
}

function runScript(workspace, script, args = []) {
  return spawnSync(process.execPath, [path.join(SCRIPT_DIR, script), ...args], {
    cwd: workspace,
    encoding: 'utf8'
  });
}

function readFile(workspace, file) {
  return fs.readFileSync(path.join(workspace, ...file.split('/')), 'utf8');
}

test('docs-sync normalizes document map paths and excludes session logs', t => {
  const workspace = createWorkspace(t, {
    'docs/index.md': `---\ntype: docs_index\nstatus: active\npriority: p1\nupdated: 2026-05-24\ncontext_policy: on_demand\nowner: project\n---\n\n# Documentation Index\n`,
    'docs/accessibility.md': `---\ntype: accessibility_policy\nstatus: active\nupdated: 2026-05-24\ncontext_policy: retrieve_when_planning\nowner: project\n---\n\n# Accessibility Policy\n`,
    'docs/memory/current.md': `---\ntype: working_memory\nstatus: active\nupdated: 2026-05-24\ncontext_policy: always_retrievable\nowner: project\n---\n\n# Current Project Memory\n`,
    'docs/memory/sessions/YYYY-MM-DD.md': `---\ntype: session_log\nstatus: archive\nupdated: 2026-05-24\ncontext_policy: historical\nowner: project\n---\n\n# Session Log: YYYY-MM-DD\n`,
    'docs/adr/0000-template.md': `---\ntype: adr\nstatus: template\nupdated: 2026-05-24\ncontext_policy: on_demand\nowner: project\n---\n\n# ADR-0000: Decision Title\n`,
    'docs/adr/0001-example.md': `---\ntype: adr\nstatus: proposed\nupdated: 2026-05-24\ncontext_policy: on_demand\nowner: project\n---\n\n# ADR-0001: Example Decision\n`
  });

  const result = runScript(workspace, 'docs-sync.mjs');
  assert.equal(result.status, 0, result.stderr || result.stdout);

  const index = readFile(workspace, 'docs/index.md');
  const decisions = readFile(workspace, 'docs/decisions.md');

  assert.match(index, /`docs\/accessibility\.md`/);
  assert.doesNotMatch(index, /docs\\accessibility\.md/);
  assert.doesNotMatch(index, /docs\/memory\/sessions\//);
  assert.match(decisions, /\| `0000` \| template \| ADR-0000: Decision Title \|/);
  assert.match(decisions, /\| `0001` \| proposed \| ADR-0001: Example Decision \|/);
});

test('docs-completed-regen preserves the updated date when content is unchanged', t => {
  const completed = `---\ntype: task_archive_index\nstatus: archive\npriority: p3\nupdated: 2024-01-02\ncontext_policy: on_demand\nowner: project\n---\n\n# Completed Task Index\n\n<!-- AUTO-GENERATED by scripts/docs-completed-regen.mjs -->\n\nNo completed task markers found yet.\n`;
  const workspace = createWorkspace(t, {
    'docs/tasks/completed.md': completed,
    'docs/memory/sessions/.gitkeep': ''
  });

  const result = runScript(workspace, 'docs-completed-regen.mjs');
  assert.equal(result.status, 0, result.stderr || result.stdout);
  assert.match(result.stdout, /unchanged/);
  assert.equal(readFile(workspace, 'docs/tasks/completed.md'), completed);
});

