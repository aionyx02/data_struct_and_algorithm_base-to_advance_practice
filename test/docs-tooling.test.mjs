test('docs-new-adr creates the next proposed ADR from a title', t => {
  const workspace = createWorkspace(t, {
    'docs/adr/0000-template.md': `---\ntype: adr\nstatus: template\nupdated: 2026-05-24\ncontext_policy: on_demand\nowner: project\n---\n\n# ADR-0000: Decision Title\n`,
    'docs/adr/0001-existing-decision.md': `---\ntype: adr\nstatus: proposed\nupdated: 2026-05-24\ncontext_policy: on_demand\nowner: project\n---\n\n# ADR-0001: Existing Decision\n`
  });

  const result = runScript(workspace, 'docs-new-adr.mjs', ['Security Boundary Review']);
  assert.equal(result.status, 0, result.stderr || result.stdout);

  const file = 'docs/adr/0002-security-boundary-review.md';
  const content = readFile(workspace, file);
  assert.match(result.stdout, new RegExp(`created ${file}`));
  assert.match(content, /status: proposed/);
  assert.match(content, /# ADR-0002: Security Boundary Review/);
  assert.match(content, /## Security Review/);
  assert.match(content, /## Resource Impact/);
});

