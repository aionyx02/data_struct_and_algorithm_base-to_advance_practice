import { read, walk, writeGeneratedFile } from './docs-utils.mjs';

const rows = [];
for (const file of walk('docs/memory/sessions', f => f.endsWith('.md') && !f.endsWith('YYYY-MM-DD.md'))) {
  const content = read(file);
  for (const line of content.split('\n')) {
    const m = line.match(/^## COMPLETED:\s*([^\s]+)\s*-\s*(.+)$/);
    if (m) rows.push({ task: m[1], summary: m[2], file });
  }
}

let body = '';

if (!rows.length) {
  body += 'No completed task markers found yet.\n';
} else {
  body += '| Task | Summary | Session |\n|---|---|---|\n';
  for (const r of rows) body += `| \`${r.task}\` | ${r.summary} | \`${r.file}\` |\n`;
}

const changed = writeGeneratedFile(
  'docs/tasks/completed.md',
  updated => `---\ntype: task_archive_index\nstatus: archive\npriority: p3\nupdated: ${updated}\ncontext_policy: on_demand\nowner: project\n---\n\n# Completed Task Index\n\n<!-- AUTO-GENERATED by scripts/docs-completed-regen.mjs -->\n\n${body}`
);
console.log(`completed index regenerated: ${rows.length} item(s), ${changed ? 'updated' : 'unchanged'}`);
